    public boolean validaCampos() {
        if (!(jcbxPessoa.getSelectedIndex() >= 1)) {
            JOptionPane.showMessageDialog(null, "Informe a pessoa!");
            jcbxPessoa.requestFocus();
            return false;
        }
        if (!(jcbxProduto.getSelectedIndex() >= 1)) {
            JOptionPane.showMessageDialog(null, "Informe o produto!");
            jcbxProduto.requestFocus();
            return false;
        }
        if (!(jtfQtd.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a quantidade do empréstimo!");
            jtfQtd.requestFocus();
            return false;
        }
        return true;
    }

    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jtbEmprestimo.getModel();
        modelo.setNumRows(0);

        for (ModeloMovimentacao e : dao.readEprestimo()) {

            modelo.addRow(new Object[]{
                e.getIdMovimentacao(),
                e.getDataMovimentacaoSTR(),
                e.getQtdMovimentacao(),
                e.getTempProduto(),
                e.getTemPessoa(),
                e.getSituacao(),
                e.getDataDevolucaoSTR()

            });
        }

    }

