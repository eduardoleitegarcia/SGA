    private void jbtnSalvarProd4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarProd4ActionPerformed
        //ação do botão salvar empréstimo

        if (validaCampos()) {

            pessoa = (ModeloPessoa) jcbxPessoa.getSelectedItem();

            movimentacao.setFKpessoa(pessoa.getIdPessoa());

            produto = (ModeloProduto) jcbxProduto.getSelectedItem();
            movimentacao.setFKproduto(produto.getIdProduto());

            movimentacao.setTipoMovimentacao("Empréstimo");
            movimentacao.setSituacao("Pendente");

            movimentacao.setQtdMovimentacao(Integer.parseInt(jtfQtd.getText()));

            //  java.util.Date RecebeData = jdcData.getDate();
            // movimentacao.setDataMovimentacao(RecebeData);
            movimentacao.setDataMovimentacaoSTR(new SimpleDateFormat("yyyy/MM/dd HH:mm").format(new Date(System.currentTimeMillis())));

            if (jtaDescricao.getText() == null) {
                movimentacao.setDescricaoMovimentacao("Nenhuma");
            } else {
                movimentacao.setDescricaoMovimentacao(jtaDescricao.getText());
            }

            //  daopess.obterPessoa(pessoa);
            daop.obterProduto(produto);

            if (produto.getQtdEstoque() > 0 && movimentacao.getQtdMovimentacao() <= produto.getQtdEstoque()) {
                dao.insereSaidaEmprestimo(movimentacao);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Quantia insuficiente no estoque!");
            }

            jcbxPessoa.setSelectedIndex(0);
            jcbxProduto.setSelectedIndex(0);
            jtfQtd.setText("");
            jtaDescricao.setText("");

            readJTable();
            jTabbedPane1.setSelectedIndex(0);

        }


    }//GEN-LAST:event_jbtnSalvarProd4ActionPerformed

