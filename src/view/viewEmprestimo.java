    private void jbtnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDevolverActionPerformed
        // ação do botão devolver

        if (jtbEmprestimo.getSelectedRow() != -1) {
            if ((jtbEmprestimo.getValueAt(jtbEmprestimo.getSelectedRow(), 6)) != null) {
                JOptionPane.showMessageDialog(null, "Empréstimo já foi devolvido!");
            } else {

                movimentacao.setIdMovimentacao((int) jtbEmprestimo.getValueAt(jtbEmprestimo.getSelectedRow(), 0));
                movimentacao.setTempProduto((String) jtbEmprestimo.getValueAt(jtbEmprestimo.getSelectedRow(), 3));
                movimentacao.setQtdMovimentacao((int) jtbEmprestimo.getValueAt(jtbEmprestimo.getSelectedRow(), 2));
                movimentacao.setDataDevolucaoSTR(new SimpleDateFormat("yyyy/MM/dd HH:mm").format(new Date(System.currentTimeMillis())));

                dao.obterIdProduto(movimentacao);

                dao.DevolveEmpréstimo(movimentacao);
                readJTable();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um empréstimo!");
        }


    }//GEN-LAST:event_jbtnDevolverActionPerformed

